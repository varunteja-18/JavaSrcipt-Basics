let students = 30;
students = students + 1; //traditional addition
students++; // increment operator
students += 1; // addition
students -= 1; //subtraction
students *= 2; //multiplication
students /= 2; //division
students %= 3; //modulus for reminder
students **= 2; // exponentiation or power
console.log(students);


